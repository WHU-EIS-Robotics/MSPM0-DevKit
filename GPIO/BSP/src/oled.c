// ---------------------------------------------模拟IIC
void IIC_Start()
{

	OLED_SCLK_Set() ;
	delay_cycles(2);
	OLED_SDIN_Set();
	delay_cycles(2);
	OLED_SDIN_Clr();
	delay_cycles(2);
	OLED_SCLK_Clr();
	delay_cycles(2);
}

void IIC_Stop()
{

	OLED_SCLK_Clr();
	delay_cycles(2);
	OLED_SDIN_Clr();
	delay_cycles(2);
	OLED_SDIN_Set();
	delay_cycles(2);
	OLED_SCLK_Set();
	delay_cycles(2);
}

void Write_IIC_Byte(unsigned char IIC_Byte)
{
	unsigned char i;
	unsigned char m,da;
	da=IIC_Byte;
	for(i=0;i<8;i++)		
	{
		m=da;
		OLED_SCLK_Clr();
		delay_cycles(2);
		m=m&0x80;
		if(m==0x80)
		{OLED_SDIN_Set();}
		else OLED_SDIN_Clr();
		delay_cycles(2);
		da=da<<1;
		OLED_SCLK_Set();
		delay_cycles(2);
	}
	OLED_SCLK_Clr();
	delay_cycles(2);
	OLED_SCLK_Set() ;
	delay_cycles(2);
}

void OLED_WR_Byte(uint8_t data,uint8_t cmd)
{
	if(cmd)
	{
		IIC_Start();
		Write_IIC_Byte(0x78);			//D/C#=0; R/W#=0
		Write_IIC_Byte(0x40);			//write data
		Write_IIC_Byte(data);
		IIC_Stop();
	}
	else 
	{
		IIC_Start();
		Write_IIC_Byte(0x78);            //Slave address,SA0=0
		Write_IIC_Byte(0x00);			//write command
		Write_IIC_Byte(data); 
		IIC_Stop();
	}
}
